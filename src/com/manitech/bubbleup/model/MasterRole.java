public class MasterRole {

	private String masterRoleId;
	private String masterRoleName;
	private String status;
	private String roleId;
	private String roleName;
	private String roleDescription;
	
	
	public String getMasterRoleId() {
		return masterRoleId;
	}
	public void setMasterRoleId(String masterRoleId) {
		this.masterRoleId = masterRoleId;
	}
	public String getMasterRoleName() {
		return masterRoleName;
	}
	public void setMasterRoleName(String masterRoleName) {
		this.masterRoleName = masterRoleName;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getRoleId() {
		return roleId;
	}
	public void setRoleId(String roleId) {
		this.roleId = roleId;
	}
	public String getRoleName() {
		return roleName;
	}
	public void setRoleName(String roleName) {
		this.roleName = roleName;
	}
	public String getRoleDescription() {
		return roleDescription;
	}
	public void setRoleDescription(String roleDescription) {
		this.roleDescription = roleDescription;
	}
	
	
	
}
